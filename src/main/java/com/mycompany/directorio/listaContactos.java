    public void showContacts(){
        
        jTextArea1.setText("");
        
        Contactoform form = new Contactoform();
        ArrayList<Contacto> array = Contactoform.listaContactos;
        int numberOfResults = array.size();

        if(numberOfResults != 0){
            for(int i = 0; i < numberOfResults; i ++){
        
            String name = array.get(i).nombre;
            String email = array.get(i).correoelectronico;
            String streetAddress = array.get(i).direccionpostal;
            String phoneNumber = array.get(i).telefono;
            
            jTextArea1.append("\n====================");
            jTextArea1.append("\nNombre: " + name);
            jTextArea1.append("\nNumero: " + phoneNumber);
            jTextArea1.append("\nCorreo: " + email);
            jTextArea1.append("\nDireccion: " + streetAddress);
            jTextArea1.append("\n====================");
               
            }
        }
    
    }
    
    
    public void addElementToDelete(){
        
        listUsers.removeAllItems();
        
        Contactoform form = new Contactoform();
        ArrayList<Contacto> array = Contactoform.listaContactos;
        int numberOfResults = array.size();
        
        for(int i = 0; i < numberOfResults; i ++){
        
            String nameUser = array.get(i).nombre;
            
            listUsers.addItem(nameUser);
               
            }
    }

