    private void filterByNameMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_filterByNameMouseClicked
        
        jTextArea1.setText("");
        
        String name = JOptionPane.showInputDialog("Nombre de la persona que desea buscar:");
        
        Contactoform form = new Contactoform();
        ArrayList<Contacto> array = Contactoform.listaContactos;
        int numberOfResults = array.size();

        if(numberOfResults != 0){
            for(int i = 0; i < numberOfResults; i ++){
        
            String nameToFind = array.get(i).nombre;
            
                if(nameToFind.equals(name)){
                    
                    String nameFilter = array.get(i).nombre;
                    String emailFilter = array.get(i).correoelectronico;
                    String streetAddressFilter = array.get(i).direccionpostal;
                    String phoneNumberFilter = array.get(i).telefono;

                    jTextArea1.append("\n====================");
                    jTextArea1.append("\nNombre: " + nameFilter);
                    jTextArea1.append("\nNumero: " + phoneNumberFilter);
                    jTextArea1.append("\nCorreo: " + emailFilter);
                    jTextArea1.append("\nDireccion: " + streetAddressFilter);
                    jTextArea1.append("\n====================");
                }
            }
        }
        
    }//GEN-LAST:event_filterByNameMouseClicked

    private void deleteOptionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteOptionMouseClicked
        
        String option = (String) listUsers.getSelectedItem();
        Contactoform form = new Contactoform();
        ArrayList<Contacto> array = Contactoform.listaContactos;
        int numberOfResults = array.size();
        
        for(int i = 0; i < numberOfResults; i ++){
        
            String nameToFind = array.get(i).nombre;
            
                if(nameToFind.equals(option)){
                    
                    jTextArea1.setText("");
                    jTextArea1.append("\n====================");
                    jTextArea1.append("\nContacto eliminado");
                    jTextArea1.append("\n====================");
                    array.remove(i);
                    addElementToDelete();
                    
                }
            }
       

    }//GEN-LAST:event_deleteOptionMouseClicked

